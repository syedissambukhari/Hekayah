// Helper function to get images for a product
export function getProductImages(sectionId: string, slug: string) {
  const section = imageMapping[sectionId as keyof typeof imageMapping]
  if (!section) return { main: [], variations: [] }
  
  const product = section[slug as keyof typeof section]
  if (!product) return { main: [], variations: [] }
  
  return product
}

// Helper function to get all available sections
export function getAvailableSections() {
  return Object.keys(imageMapping)
}

// Helper function to get all products for a section
export function getProductsForSection(sectionId: string) {
  const section = imageMapping[sectionId as keyof typeof imageMapping]
  if (!section) return []
  
  return Object.keys(section)
}
